@Slf4j
@RestController
@RequestMapping("api/pc/login")
@Api(tags = "PC用户登录注册控制器")
public class PcLoginController {

    @Autowired
    private LoginService loginService;

    @ApiOperation(value = "获取登录页图片")
    @RequestMapping(value = "/getLoginPic", method = RequestMethod.GET)
    public CommonResult<Map<String, Object>> getLoginPic() {
        return CommonResult.success(loginService.getPcLoginPic());
    }
}
